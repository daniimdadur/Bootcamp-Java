package TipeDataNumber;

public class IntegerNumber {
    public static void main(String[] args) {
        //integer number
        byte iniByte = 127;
        short iniShort = 32767;
        int iniInt = 2147483647;
        long iniLong = 9223372036554775807L;

        //floating point number
        float iniFloat = 10.12F;
        double iniDouble = 10.2424;

        //kode literals
        int decimalInt = 25;
        int hexaDecimal = 0xA12345B;
        int binInt = 0b101010;

        //kode underscore
        long balance = 1_000_000_000L;
        int sum = 1_000_000_000;



    }
}
