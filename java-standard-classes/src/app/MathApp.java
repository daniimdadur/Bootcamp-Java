package app;

public class MathApp {
    public static void main(String[] args) {
        //merupakan class utilities yg berisikan banyak sekali static method untuk operasi numerik, example trigonometri,logarithm,akar pangkat,dll.
        int min = Math.min(1000, 2000); //mengambil nilai terkecil.
        System.out.println(min);

        int max = Math.max(1000, 2000); //mengmbil nilai terbesar.
        System.out.println(max);

        System.out.println(Math.PI);
    }
}
